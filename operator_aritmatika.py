a, b = 15 , 10


# Penjumlahan
print(a ,"+" ,b, "=" , a + b)
# pengurangan
print(a ,"-" ,b, "=" , a - b)
# perkalian
print(a ,"X" ,b, "=" , a * b)
# pembagian
print(a ,":" ,b, "=" , a / b)
# modulus
print(a ,"-" ,b, "=" , a % b)
# perpangkatan
print(a ,"^" ,b, "=" , a ** b)
# pembagian bulat => Membulatkan angka di belakang koma
print(a ,"" ,b, "=" , a // b)
